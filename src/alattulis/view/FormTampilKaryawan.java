    private void mnDeletePPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnDeletePPActionPerformed
        int result = JOptionPane.showConfirmDialog(rootPane,
            "Apakah data ini akan dihapus ?",
            "Dialog Hapus", JOptionPane.YES_NO_OPTION);
        if(result == JOptionPane.YES_OPTION){
            String nik = tblKaryawan.getModel().getValueAt(tblKaryawan.getSelectedRow(), 0).toString();
            controllerLogin.deleteLogin(nik);
            controllerKaryawan.deleteKaryawan(nik);
            
        }
    }//GEN-LAST:event_mnDeletePPActionPerformed

