    private void setTableKaryawan(List<modelKaryawan> listmodelKaryawan) {
       if (listmodelKaryawan==null){
           JOptionPane.showMessageDialog(this, "Data karyawan gagal diambil dari database",
                   "DATABASE FAILED",JOptionPane.ERROR_MESSAGE);
       }else{
           DefaultTableModel tableModel = new DefaultTableModel();
           tableModel.setColumnIdentifiers(
               new String[] {
                   "NIK",
                   "Nama Karyawan",
                   "Lahir Karyawan",
                   "Kelamin Karyawan",
                   "Alamat Karyawan",
                   "Email Karyawan"
               }
           );

           for (modelKaryawan g : listmodelKaryawan) {
               Object[] o = new Object[6];
               o[0] = g.getNik();
               o[1] = g.getNama_karyawan();
               o[2] = g.getLahir_karyawan();
               o[3] = g.getKelamin_karyawan();
               o[4] = g.getAlamat_karyawan();
               o[5] = g.getEmail_karyawan();
               tableModel.addRow(o);
           }
           tblKaryawan.setAutoResizeMode( javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS );
           tblKaryawan.setModel(tableModel);
       }
    }
