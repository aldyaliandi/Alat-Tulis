    private void txtJumBayKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJumBayKeyTyped
        if(evt.getKeyCode() == 0){
           if(!txtJumBay.getText().equals("")){
                int uang = Integer.parseInt(txtJumBay.getText());
                int totbay = Integer.parseInt(lblTotBel.getText());
                float change = uang - totbay;
                lblKembalian.setText(change < 0 ? 0+"" : change+"");
            }
        }
    }//GEN-LAST:event_txtJumBayKeyTyped

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
       
        int idxrow = tblResult.getSelectedRow();
        
        String id_transaksi = lblIdTransaksi.getText();
        int id_karyawan = Integer.parseInt(lblIdKaryawan.getText().toString());
        int id_barang = Integer.parseInt(tblTransaksi.getModel().getValueAt(idxrow, 1).toString());
        String kode_barang = (tblTransaksi.getModel().getValueAt(idxrow, 2).toString());
        String tanggal_transaksi = lblTanggal.getText();
        int sub_total = Integer.parseInt(lblTotBel.getText());
        int jumlah_bayar = Integer.parseInt(txtJumBay.getText().toString());
        
        modelTransaksi tran = new modelTransaksi(
                id_transaksi,
                id_karyawan,
                id_barang,
                kode_barang,
                tanggal_transaksi,
                sub_total,
                jumlah_bayar                       
            );
            controllerTransaksi.insertmodelTransaksi(tran);
            
            setClear();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void setTableBarang(List<modelBarang> listBarang) {
       if (listBarang==null){
           JOptionPane.showMessageDialog(this, "Data barang gagal diambil dari database","DATABASE FAILED",JOptionPane.ERROR_MESSAGE);
       }else{
           DefaultTableModel tableModel = new DefaultTableModel();
           tableModel.setColumnIdentifiers(
               new String[] {
                   "Id Barang",
                   "Nama Barang",
                   "Kode Barang",
                   "Harga Barang",
                   "Stok Barang"
               }
           );

           for (modelBarang s : listBarang) {
               Object[] o = new Object[5];
               o[0] = s.getId_barang();
               o[1] = s.getNama_barang();
               o[2] = s.getKode_barang();
               o[3] = s.getHarga_barang();
               o[4] = s.getStok_barang();
               tableModel.addRow(o);
           }
           tblResult.setAutoResizeMode( javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS );
           tblResult.setModel(tableModel);
       }
    }
    
