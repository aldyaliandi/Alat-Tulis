    private void txtPencarianKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPencarianKeyTyped
        String colName = tblResult.getColumnModel().getColumn(0).getHeaderValue().toString();
        if(colName.equals("NIK")){
            setTableKaryawan(controllerKaryawan.getAllKaryawanByName(txtPencarian.getText()));
        } else{
            setTableBarang(controllerBarang.getAllBarangByName(txtPencarian.getText()));
        }
    }//GEN-LAST:event_txtPencarianKeyTyped

    private void tblResultMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResultMousePressed
        this.setLocationRelativeTo(null);
        if(evt.getClickCount() == 2){
            String colName = tblResult.getColumnModel().getColumn(0).getHeaderValue().toString();
            int idxrow = tblResult.getSelectedRow();
            if(colName.equals("NIK")){
                btnNamaKar.setText(tblResult.getModel().getValueAt(idxrow, 1).toString());
                lblIdKaryawan.setText(tblResult.getModel().getValueAt(idxrow, 0).toString());
            }else{
                
                String id_transaksi = lblIdTransaksi.getText();
                int id_karyawan = 0;
                int id_barang = Integer.parseInt(tblResult.getModel().getValueAt(idxrow, 0).toString());
                String kode_barang = tblResult.getModel().getValueAt(idxrow, 2).toString();
                String tanggal_transaksi = "";
                int sub_total = Integer.parseInt(tblResult.getModel().getValueAt(idxrow,3).toString());
                int jumlah_bayar = 0;

                modelTransaksi data = new modelTransaksi (id_transaksi, id_karyawan, 
                                                         id_barang, kode_barang, tanggal_transaksi,
                                                         sub_total, jumlah_bayar);
                
                totalpembelian += sub_total;
                lblTotBel.setText(totalpembelian+"") ;
                
                collection.add(data);
            }
            searchData.dispose();
            setTableTransaksi(collection);
        }
    }//GEN-LAST:event_tblResultMousePressed

    private void btnKurBarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKurBarActionPerformed
        if(collection.size() >= 0){
            totalpembelian -= collection.get(0).getSub_total(); 
            lblTotBel.setText(totalpembelian+"");
            collection.remove(0);
            setTableTransaksi(collection);
        }
    }//GEN-LAST:event_btnKurBarActionPerformed

