    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        int id_barang = Integer.parseInt(txtIdBarang.getText());
        String nama_barang = txtNamaBarang.getText();
        String kode_barang = txtKodeBarang.getText();
        int harga_barang = Integer.parseInt(txtHargaBarang.getText());
        int stok_barang = Integer.parseInt(txtStokBarang.getText());

        modelBarang data = new modelBarang(
            id_barang,
            nama_barang,
            kode_barang,
            harga_barang,
            stok_barang
        );

        if(btnSimpan.getText().equals("Simpan")){
            if(controllerBarang.insertBarang(data)){
                lblOutput.setText("Insert Berhasil");
            }else{
                lblOutput.setText("Insert Gagal");
            }
        } else{
            if(controllerBarang.updateBarang(data)){
                lblOutput.setText("Update Berhasil");
            }else{
                lblOutput.setText("Update Gagal");
            }
        }
        setClear();
    }//GEN-LAST:event_btnSimpanActionPerformed

