    private void tblBarangMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBarangMousePressed
        if(evt.getClickCount() == 2){
            int idxrow = tblBarang.getSelectedRow();
            txtIdBarang.setText(tblBarang.getModel().getValueAt(idxrow, 0).toString());
            txtNamaBarang.setText(tblBarang.getModel().getValueAt(idxrow, 1).toString());
            txtKodeBarang.setText(tblBarang.getModel().getValueAt(idxrow, 2).toString());
            txtHargaBarang.setText(tblBarang.getModel().getValueAt(idxrow, 3).toString());
            txtStokBarang.setText(tblBarang.getModel().getValueAt(idxrow, 4).toString());
            btnSimpan.setText("Update");
        }
        if(evt.isPopupTrigger()){
            puTable.show(tblBarang, evt.getX(), evt.getY());
        }
    }//GEN-LAST:event_tblBarangMousePressed

