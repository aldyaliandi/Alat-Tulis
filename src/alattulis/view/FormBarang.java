    private void setTableBarang(List<modelBarang> listBarang) {
       if (listBarang==null){
           JOptionPane.showMessageDialog(this, "Data barang gagal diambil dari database",
                   "DATABASE FAILED",JOptionPane.ERROR_MESSAGE);
       }else{
           DefaultTableModel tableModel = new DefaultTableModel();
           tableModel.setColumnIdentifiers(
               new String[] {
                   "Id Barang",
                   "Nama Barang",
                   "Kode Barang",
                   "Harga Barang",
                   "Stok Barang"
               }
           );

           for (modelBarang s : listBarang) {
               Object[] o = new Object[5];
               o[0] = s.getId_barang();
               o[1] = s.getNama_barang();
               o[2] = s.getKode_barang();
               o[3] = s.getHarga_barang();
               o[4] = s.getStok_barang();
               tableModel.addRow(o);
           }
           tblBarang.setAutoResizeMode( javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS );
           tblBarang.setModel(tableModel);
       }
    }
    
